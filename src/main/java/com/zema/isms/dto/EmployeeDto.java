package com.zema.isms.dto;

import com.zema.isms.domain.Department;
import com.zema.isms.validator.UniqueEmployeeCode;
import javax.validation.constraints.NotNull;

/**
 *
 * @author uppert
 */
public class EmployeeDto {
        
    private String email;
         
    private String employeeId;
    
     @NotNull(message = "first  name is not null")
   private String firstName;
 
      @NotNull(message = "middle  name is not null")
    private String middleName;
        @NotNull(message = "last  name is not null")
    private String lastName;

    private Integer age;

    private String gender;

    
    private String phone;
      @UniqueEmployeeCode
    private String code; 
 

    private String jobPosition;
     private Department department;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getJobPosition() {
        return jobPosition;
    }

    public void setJobPosition(String jobPosition) {
        this.jobPosition = jobPosition;
    }

    public Department getDepartment() {
        return department;
    }

    public void setDepartment(Department department) {
        this.department = department;
    }

    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }
     
 
}
